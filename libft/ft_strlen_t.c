#include "libft.h"

int	ft_strlen_t(const char *s)
{
	int	i;

	i = 0;
	if (s == NULL)
		return (0);
	while (s[i])
	{
		if (s[i] == '\t')
			i += 4;
		else
			i++;
	}
	return (i);
}
